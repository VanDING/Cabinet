const computed = entries.map((e) => {
  const vb = e.watermarkViewBox ?? '0 0 24 24';
  return {
    match: e.match,
    info: {
      dataUri: createAvatar(lorelei, { seed: seedDedup[e.seed], size: 128 }).toDataUri(),
      gradient: `linear-gradient(135deg, ${e.gradient[0]}, ${e.gradient[1]})`,
      label: e.label,
      watermark: svgWM(e.watermarkSvg, vb),
      watermarkSvg: `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${vb}" fill="currentColor">${e.watermarkSvg}</svg>`,
      watermarkViewBox: vb,
      brandColor: e.gradient[0],
    } satisfies AgentIconInfo,
  };
});

export function getAgentIcon(name: string, model?: string): AgentIconInfo | null {
  for (const entry of computed) {
    if (entry.match(name, model)) return entry.info;
  }
  return null;
}
